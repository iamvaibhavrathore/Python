l=[1,3,7,2,6,4,8,5,9]

print("***** count *****" )
c=l.count(4)
print(c)

print("****** Max *****")
m=max(l)  # also applicable for char value too
print(m)

print("******* min ******")
m=min(l)
print(m)

print("******** sort ******")
l.sort()
print(l)

print("******* reverse *****")
l.reverse() # same applicable for char value
print(l)

print("******* index ******")
a=l.index(6)
print(a)